console.log("10"/ "2");

//resultado com casa decimal - ponto flutuante
console.log("7" / "2");

//NaN: not a number - não é possível fazer essa operação
console.log("Thiane" / "2");

//numero com ponto flutuante
console.log(6.5);
//dá um espaço
console.log(6,5);